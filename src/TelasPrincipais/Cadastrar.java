    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        //1 - Pegar os dados digitados nos campos e guardar em variaveis.    
        String nom, sob, ema, sen, pes, ida, alt, obj,id_cli;
        id_cli = null;
        nom = txtNome.getText();
        sob = txtSobrenome.getText();
        ema = txtEmail.getText();
        sen = pswSenha.getText();
        pes = txtPeso.getText();
        ida = txtIdade.getText();
        alt = txtAltura.getText();
        obj = cmbObjetivo.getSelectedItem().toString();
        // 2 - Conectar ao banco de dados
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conectar = DriverManager.getConnection("jdbc:mysql://localhost:3307/clinica_saude", "root","p@$$w0rd");
            // 3 - Enviar os dados para a tabela do Banco de dados
            PreparedStatement st = conectar.prepareStatement("INSERT INTO usuarios VALUES(?,?,?,?,?,?,?,?,?)");
            st.setString(1, id_cli);
            st.setString(2, nom);
            st.setString(3, sob);
            st.setString(4, ema);
            st.setString(5, sen);
            st.setString(6, pes);
            st.setString(7, ida);
            st.setString(8, alt);
            st.setString(9, obj);
            st.executeUpdate();
            // 4 - Enviar mensagem para o usuario
            JOptionPane.showMessageDialog(null, "Usu√°rio cadastrado!");
            // 5 - Limpar os campos
            txtNome.setText("");
            txtSobrenome.setText("");
            txtEmail.setText("");
            pswSenha.setText("");
            txtPeso.setText("");
            txtIdade.setText("");
            txtAltura.setText("");
            txtNome.requestFocus();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Entre em contato com o suporte e informe o erro: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

